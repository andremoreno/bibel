<h4 class="text-2xl mb-3">Hata Haporseaon</h4>

<p class="mb-3">
    Ahu porsea di Debata Jahowa, I do Ama pargogo na so hatudosan, na tumompa
    langit dohot tano.
</p>

<p class="mb-3">
    Ahu porsea di Jesus Kristus Anak ni Debata Jahowa nasasada i; i ma na tinubuhon 
    ni si Maria, na gabegabean sian Tondi Parbadia andorang so habubuhan. I do 
    Tuhanta na tumaon na bernit di panguhuman ni si Latus, na mate tarpajal do 
    Ibana di hau pinarsilang, na tuat tu banuatoru dung ditanom, na mulak mangolu 
    di ari patoluhon, na manaek tu Surgo laho hundul tu siamun ni Debata Jahowa, 
    AmaNa i pargogo na so hatudosan i, disi ma Ibana paima mulak sogot tu tano on 
    manguhumi halak na mangolu dohot na mate.
</p>

<p class="mb-3">
    Ahu porsea di Tondi Parbadia, jala adong sada Huria na badia, Huria hatopan ni 
    halak Kristen angka na badia, dohot di hasesaan ni dosa dohot di Hata i na 
    mandok: Mulak mangolu do sogot daging ni halak naung mate, dohot di hangoluan 
    na so ra suda.
</p>

